package com.sg.superheroes.model;

/**
 *
 * @author chandler
 */
public class MetaHuman {

    private String metaHumanID;
    private String name;
    private String identity;

    public String getMetaHumanID() {
        return metaHumanID;
    }

    public void setMetaHumanID(String metaHumanID) {
        this.metaHumanID = metaHumanID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdentity() {
        return identity;
    }

    public void setIdentity(String identity) {
        this.identity = identity;
    }

}
